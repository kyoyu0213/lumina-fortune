export function pickRandomCards(count: number): TarotCardData[] {
  const shuffled = [...TAROT_CARDS].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, count).map((card) => ({
    ...card,
    reversed: Math.random() > 0.7,
  }))
}

function detectCategory(text: string): string {
  if (/恋|愛|好き|彼氏|彼女|結婚|片思い/.test(text)) return "恋愛"
  if (/仕事|キャリア|転職|職場|出世/.test(text)) return "仕事"
  if (/健康|体|病気|元気|疲れ/.test(text)) return "健康"
  if (/金|お金|運|貯金|投資|収入/.test(text)) return "金運"
  return "default"
}

function getCardReading(
  cardId: string,
  category: string,
  reversed: boolean
): string {
  const cardData = CARD_READINGS[cardId] ?? CARD_READINGS.fool
  const categoryData = cardData[category] ?? cardData.default
  return reversed ? categoryData.reversed : categoryData.upright
}

export function getFortuneResponse(
  userInput: string
): { text: string; cards: TarotCardData[] } {
  const cards = pickRandomCards(3)
  const category = detectCategory(userInput)

  const intros = INTRO_PHRASES[category] ?? INTRO_PHRASES.default
  const intro = intros[Math.floor(Math.random() * intros.length)]

  const readings = cards.map(
    (card, i) =>
      `【${i + 1}枚目】${card.reversed ? "逆位置の" : ""}「${card.name}」\n${getCardReading(card.id, category, card.reversed)}`
  )

  const closing =
    CLOSING_PHRASES[Math.floor(Math.random() * CLOSING_PHRASES.length)]

  const text = `${intro}\n\n${readings.join("\n\n")}\n\n${closing}`

  return { text, cards }
}

